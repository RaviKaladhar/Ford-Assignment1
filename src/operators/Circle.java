package operators;

public class Circle {
    public static void main(String args[])
    {
        int radius=6;
        Double area=Math.PI*radius*radius;
        Double circumference=2*Math.PI*radius;
        System.out.println("area of circle with radius 6 is:"+area);
        System.out.println("circumference of circle with radius 6 is:"+circumference);
    }
}
